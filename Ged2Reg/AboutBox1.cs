namespace Ged2Reg
{
    partial class AboutBox1 : Form
    {
        public AboutBox1()
        {
            InitializeComponent();
            this.Text = $"About {AssemblyTitle}";
            this.labelProductName.Text = AssemblyProduct;
            //if (System.Deployment.Application.ApplicationDeployment.IsNetworkDeployed)
            //{
